    def _round_geometry_coordinates(self, geom: QgsGeometry, decimals: int = 6) -> QgsGeometry:
        """
        Round all polygon / multipolygon coordinates to the given number of decimals.
        Returns a new QgsGeometry.
        """
        if geom.isEmpty():
            return geom

        from qgis.core import QgsPointXY, QgsGeometry

        def round_ring(ring):
            rounded = []
            for pt in ring:
                rounded.append(QgsPointXY(round(pt.x(), decimals), round(pt.y(), decimals)))
            return rounded

        # Polygon
        if not geom.isMultipart():
            poly = geom.asPolygon()
            if not poly:
                return geom

            rounded_poly = []
            for ring in poly:
                rounded_poly.append(round_ring(ring))

            return QgsGeometry.fromPolygonXY(rounded_poly)

        # MultiPolygon
        mp = geom.asMultiPolygon()
        if not mp:
            return geom

        rounded_mp = []
        for poly in mp:
            rounded_poly = []
            for ring in poly:
                rounded_poly.append(round_ring(ring))
            rounded_mp.append(rounded_poly)

        return QgsGeometry.fromMultiPolygonXY(rounded_mp)

    def _fix_duplicate_vertices(self, geom: QgsGeometry) -> QgsGeometry:
        """
        Remove consecutive duplicate vertices from a polygon or multipolygon geometry.
        Returns a repaired QgsGeometry.
        """

        if geom.isEmpty():
            return geom

        def clean_ring(coords):
            if not coords:
                return coords

            cleaned = [coords[0]]
            for i in range(1, len(coords)):
                if coords[i] != coords[i - 1]:
                    cleaned.append(coords[i])

            # Ensure closure
            if cleaned[0] != cleaned[-1]:
                cleaned.append(cleaned[0])

            return cleaned

        from qgis.core import QgsPointXY, QgsGeometry

        # ---- Handle simple Polygon ----
        if geom.isMultipart() is False:
            poly = geom.asPolygon()   # [[(x,y), (x,y), ...], [hole], ...]
            if not poly:
                return geom

            new_rings = []
            for ring in poly:
                coords = [(pt.x(), pt.y()) for pt in ring]
                new_coords = clean_ring(coords)
                new_rings.append([QgsPointXY(x, y) for x, y in new_coords])

            return QgsGeometry.fromPolygonXY(new_rings)

        # ---- Handle MultiPolygon ----
        else:
            mp = geom.asMultiPolygon()  # list of polygons → each polygon = list of rings
            if not mp:
                return geom

            new_polygons = []
            for poly in mp:
                new_rings = []
                for ring in poly:
                    coords = [(pt.x(), pt.y()) for pt in ring]
                    new_coords = clean_ring(coords)
                    new_rings.append([QgsPointXY(x, y) for x, y in new_coords])
                new_polygons.append(new_rings)

            return QgsGeometry.fromMultiPolygonXY(new_polygons)


    def _has_z_values(self, geom: QgsGeometry) -> bool:
        """
        Detect real Z-enabled geometries based on WKB type.
        """
        return QgsWkbTypes.hasZ(geom.wkbType())
    
    # Helper to get NodeID and PlotID for a feature
    def _get_ids(self, feature, layer, node_use_existing, node_field_name,
                plot_existing, plot_field_name):
        node_val = ""
        plot_val = ""

        if node_use_existing and node_field_name:
            idx = layer.fields().indexFromName(node_field_name)
            if idx != -1:
                v = feature.attributes()[idx]
                node_val = "" if v is None else str(v)

        if plot_existing and plot_field_name:
            idx = layer.fields().indexFromName(plot_field_name)
            if idx != -1:
                v = feature.attributes()[idx]
                plot_val = "" if v is None else str(v)

        return node_val, plot_val



