    def _run_transformation_from_dialog(self):
        """Read settings from dialog and run the actual transformation."""

        export_blocked = False

        # Track all feature-level geometry fixes in a structured way
        repair_log = {}  # { feature_id : [ "message1", "message2" ] }
        
        # Track validations to reflect in csv report
        validation_rows = []

        # Validate selected layer
        idx = self.dlg.layerComboBox.currentIndex()
        if idx < 0 or idx >= len(self._polygon_layers):
            QMessageBox.warning(
                self.iface.mainWindow(),
                self.tr("TRACT Geolocation Formatter"),
                self.tr("Please select an input polygon layer."),
            )
            return

        layer = self._polygon_layers[idx]

        if layer.geometryType() not in (
            QgsWkbTypes.PolygonGeometry,
        ):
            QMessageBox.warning(
                self.iface.mainWindow(),
                self.tr("TRACT Geolocation Formatter"),
                self.tr("Selected layer is not a polygon or multipolygon layer."),
            )
            return

        field_names = [f.name() for f in layer.fields()]
        if not field_names:
            QMessageBox.warning(
                self.iface.mainWindow(),
                self.tr("TRACT Geolocation Formatter"),
                self.tr("The selected layer has no attributes."),
            )
            return

        # NodeID options
        node_use_existing = self.dlg.nodeExistingRadio.isChecked()
        node_auto = self.dlg.nodeAutoRadio.isChecked()

        # Optional prefix for auto-generated NodeIDs
        node_prefix = ""
        if node_auto:
            node_prefix = self.dlg.nodePrefixLineEdit.text() or ""

        # Must choose one method
        if not node_use_existing and not node_auto:
            QMessageBox.warning(
                self.iface.mainWindow(),
                self.tr("TRACT Geolocation Formatter"),
                self.tr("Please choose how to create NodeID (existing field or auto-generate)."),
            )
            return

        # If using an existing field, validate field selection
        node_field_name = None
        if node_use_existing:
            node_field_name = self.dlg.nodeFieldCombo.currentText()
            if not node_field_name:
                QMessageBox.warning(
                    self.iface.mainWindow(),
                    self.tr("TRACT Geolocation Formatter"),
                    self.tr("Please select a NodeID field."),
                )
                return

        # PlotID options
        plot_none = self.dlg.plotNoneRadio.isChecked()
        plot_existing = self.dlg.plotExistingRadio.isChecked()
        plot_auto = self.dlg.plotAutoRadio.isChecked()

        plot_field_name = None
        plot_prefix = ""

        if plot_existing:
            plot_field_name = self.dlg.plotFieldCombo.currentText()
            if not plot_field_name:
                QMessageBox.warning(
                    self.iface.mainWindow(),
                    self.tr("TRACT Geolocation Formatter"),
                    self.tr("Please select a PlotID field or choose 'No PlotID'."),
                )
                return
        elif plot_auto:
            plot_prefix = self.dlg.plotPrefixLineEdit.text() or ""

        # Output path
        output_path = self.dlg.outputPathLineEdit.text().strip()
        if not output_path:
            QMessageBox.warning(
                self.iface.mainWindow(),
                self.tr("TRACT Geolocation Formatter"),
                self.tr("Please choose an output GeoJSON path."),
            )
            return

        if not output_path.lower().endswith((".geojson", ".json")):
            output_path += ".geojson"
            self.dlg.outputPathLineEdit.setText(output_path)

        # self.dlg.logTextEdit.clear()
        self.dlg.logTextEdit.setPlainText(
            "This tool converts polygon geometries into TRACT's standard geolocation format.\n\n"
            "Automatic fixes applied:\n"
            "- Removal of consecutive duplicate vertices\n"
            "- Geometry validation (makeValid)\n"
            "- Reprojection to EPSG:4326\n\n"
            "Specify NodeID / PlotID and export to a TRACT-ready GeoJSON."
        )


        self._log(self.tr("Starting TRACT GeoJSON export..."))
        self._log(self.tr("Input layer: {}").format(layer.name()))
        self._log(self.tr("Output file: {}").format(output_path))

        # Prepare CRS transform to EPSG:4326
        target_crs = QgsCoordinateReferenceSystem("EPSG:4326")
        transform_context = QgsProject.instance().transformContext()
        coord_transform = None

        # Transformation to calculate areas
        area_transform = QgsCoordinateTransform(
            layer.crs(),
            AREA_CRS,
            transform_context
        )

        if layer.crs() != target_crs:
            coord_transform = QgsCoordinateTransform(layer.crs(), target_crs, transform_context)
            self._log(self.tr("Reprojecting geometries to EPSG:4326..."))
        else:
            self._log(self.tr("Layer already in EPSG:4326."))

        # Output fields
        out_fields = QgsFields()
        out_fields.append(QgsField("NodeID", QVariant.String))
        out_fields.append(QgsField("PlotID", QVariant.String))

        geom_type = layer.wkbType()
        geom_type_2d = QgsWkbTypes.dropZ(geom_type)

        writer = QgsVectorFileWriter(
            output_path,
            "UTF-8",
            out_fields,
            geom_type_2d, # needed to ensure a 2d type
            target_crs,
            "GeoJSON",
        )


        if writer.hasError() != QgsVectorFileWriter.NoError:
            QMessageBox.critical(
                self.iface.mainWindow(),
                self.tr("TRACT Geolocation Formatter"),
                self.tr("Error creating output file: {}").format(writer.errorMessage()),
            )
            del writer
            return

        invalid_features = []
        written_count = 0
        total_count = 0

        small_area_features = []  # list of (fid, area_ha)
        polygon_hole_features = []  # list of fids


        features = list(layer.getFeatures())
        features.sort(key=lambda x: x.id())  # deterministic ordering

        total_features = len(features)

        # Create progress bar in QGIS message bar
        progress_message = self.iface.messageBar().createMessage(
            "TRACT Geolocation Formatter",
            "Processing features..."
        )
        progress = QProgressBar()
        progress.setMaximum(total_features)
        progress.setValue(0)
        progress.setFormat("%v / %m")
        progress.setAlignment(Qt.AlignLeft | Qt.AlignVCenter)
        progress_message.layout().addWidget(progress)
        progress_item = self.iface.messageBar().pushWidget(progress_message, Qgis.Info)

        try:
            for i, f in enumerate(features, start=1):
                progress.setValue(i)
                QApplication.processEvents()

                total_count += 1
                geom = QgsGeometry(f.geometry())
                if geom.isEmpty():
                    invalid_features.append((f.id(), "Empty geometry"))
                    continue

                if not geom.isGeosValid():
                    fixed = geom.makeValid()
                    if fixed.isEmpty() or not fixed.isGeosValid():
                        invalid_features.append((f.id(), "Invalid geometry (cannot make valid)"))
                        continue
                    geom = fixed

                # --- Z-value removal ---
                if self._has_z_values(geom):
                    if f.id() not in repair_log:
                        repair_log[f.id()] = []
                    repair_log[f.id()].append("Removed Z values")

                    node_id, plot_id = self._get_ids(
                        f,
                        layer,
                        node_use_existing,
                        node_field_name,
                        plot_existing,
                        plot_field_name,
                    )

                    validation_rows.append({
                        "feature_id": f.id(),
                        "NodeID": node_id,
                        "PlotID": plot_id,
                        "status": "WARNING",
                        "issue_type": "z_values",
                        "message": "Removed Z values"
                    })

                # Reproject to EPSG:4326 first
                if coord_transform is not None:
                    try:
                        geom.transform(coord_transform)
                    except Exception as e:
                        invalid_features.append((f.id(), "Reprojection error: {}".format(e)))
                        continue

                # Round coordinates once, after reprojection, before checks
                rounded_geom = self._round_geometry_coordinates(geom, COORD_DECIMALS)

                if rounded_geom.isEmpty():
                    invalid_features.append((f.id(), "Geometry became empty after coordinate rounding"))
                    continue

                if not rounded_geom.equals(geom):
                    if f.id() not in repair_log:
                        repair_log[f.id()] = []
                    repair_log[f.id()].append(f"Rounded coordinates to {COORD_DECIMALS} decimals")

                    node_id, plot_id = self._get_ids(
                        f,
                        layer,
                        node_use_existing,
                        node_field_name,
                        plot_existing,
                        plot_field_name,
                    )

                    validation_rows.append({
                        "feature_id": f.id(),
                        "NodeID": node_id,
                        "PlotID": plot_id,
                        "status": "WARNING",
                        "issue_type": "coordinate_rounding",
                        "message": f"Rounded coordinates to {COORD_DECIMALS} decimals"
                    })

                geom = rounded_geom

                # Duplicate vertex check after rounding, because rounding can create duplicates
                coords = [(v.x(), v.y()) for v in geom.vertices()]
                has_bad_duplicates = False
                for j in range(len(coords) - 1):
                    if coords[j] == coords[j + 1]:
                        has_bad_duplicates = True
                        break

                if has_bad_duplicates:
                    self._log(f"Fixing consecutive duplicate vertices in feature {f.id()}...")

                    if f.id() not in repair_log:
                        repair_log[f.id()] = []

                    repair_log[f.id()].append("Removed consecutive duplicate vertices")

                    node_id, plot_id = self._get_ids(
                        f,
                        layer,
                        node_use_existing,
                        node_field_name,
                        plot_existing,
                        plot_field_name,
                    )

                    validation_rows.append({
                        "feature_id": f.id(),
                        "NodeID": node_id,
                        "PlotID": plot_id,
                        "status": "WARNING",
                        "issue_type": "duplicate_vertices",
                        "message": "Removed consecutive duplicate vertices"
                    })

                    geom = self._fix_duplicate_vertices(geom)

                    if geom.isEmpty() or not geom.isGeosValid():
                        invalid_features.append((f.id(), "Duplicate-vertex fix failed (invalid geometry)"))
                        continue

                # Minimum area check
                try:
                    area_geom = QgsGeometry(geom)
                    area_geom.transform(area_transform)
                    area_m2 = area_geom.area()
                    area_ha = area_m2 / 10000.0
                except Exception as e:
                    invalid_features.append((f.id(), f"Area computation failed: {e}"))
                    continue

                if area_ha < MIN_PLOT_AREA_HA:
                    small_area_features.append((f.id(), area_ha))

                    node_id, plot_id = self._get_ids(
                        f,
                        layer,
                        node_use_existing,
                        node_field_name,
                        plot_existing,
                        plot_field_name,
                    )

                    validation_rows.append({
                        "feature_id": f.id(),
                        "NodeID": node_id,
                        "PlotID": plot_id,
                        "status": "ERROR",
                        "issue_type": "small_area",
                        "message": f"Area below minimum ({area_ha:.4f} ha < {MIN_PLOT_AREA_HA} ha)"
                    })

                    continue

                # Polygon holes detection (report only)
                has_holes = False

                if geom.isMultipart():
                    multipoly = geom.asMultiPolygon()
                    for poly in multipoly:
                        if len(poly) > 1:  # exterior ring + one or more interior rings
                            has_holes = True
                            break
                else:
                    poly = geom.asPolygon()
                    if poly and len(poly) > 1:
                        has_holes = True

                if has_holes:
                    polygon_hole_features.append(f.id())

                    node_id, plot_id = self._get_ids(
                        f,
                        layer,
                        node_use_existing,
                        node_field_name,
                        plot_existing,
                        plot_field_name,
                    )

                    validation_rows.append({
                        "feature_id": f.id(),
                        "NodeID": node_id,
                        "PlotID": plot_id,
                        "status": "ERROR",
                        "issue_type": "polygon_holes",
                        "message": "Polygon contains interior holes"
                    })

                    continue

                new_feat = QgsFeature(out_fields)

                # NodeID
                if node_use_existing:
                    attrs = f.attributes()
                    node_idx = layer.fields().indexFromName(node_field_name)
                    val = attrs[node_idx] if node_idx != -1 else None
                    new_feat["NodeID"] = "" if val is None else str(val)
                else:
                    new_feat["NodeID"] = f"{node_prefix}{written_count + 1}"

                # PlotID
                if plot_none:
                    new_feat["PlotID"] = ""
                elif plot_existing and plot_field_name:
                    attrs = f.attributes()
                    plot_idx = layer.fields().indexFromName(plot_field_name)
                    val = attrs[plot_idx] if plot_idx != -1 else None
                    new_feat["PlotID"] = "" if val is None else str(val)
                elif plot_auto:
                    new_feat["PlotID"] = "{}{}".format(plot_prefix, written_count + 1)
                else:
                    new_feat["PlotID"] = ""

                new_feat.setGeometry(geom)

                if not writer.addFeature(new_feat):
                    invalid_features.append((f.id(), "Failed to write feature to output"))
                    continue

                written_count += 1

        finally:
            self.iface.messageBar().popWidget(progress_item)

        del writer  # flush

        self.iface.messageBar().pushMessage(
            "TRACT Geolocation Formatter",
            f"Finished processing {total_count} features.",
            level=Qgis.Success,
            duration=3
        )

